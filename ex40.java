public class ex40 {
    public static void main(String args[]){
        int a=10,b=8;
        System.out.println("a ="+a);
        System.out.println("b = "+b);
        System.out.println("a+b ="+(a+b));
        System.out.println("a-b ="+(a-b));
        System.out.println("a*b ="+(a*b));
        System.out.println("a/b ="+(a/b));
        System.out.println("a%b ="+(a%b));
        System.out.println("a>b ="+(a>b));
        System.out.println("a!=b ="+(a!=b));
        System.out.println("a==b ="+(a==b));
        System.out.println("a>b&& a!=b ="+((a>b)&&(a!=b)));
        System.out.println("a&b ="+(a&b));
        System.out.println("a^b ="+(a^b));
        System.out.println("a.greatest val ="+((a>b)?a:b));

    }
}
