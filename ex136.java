// Wroking with toUpper() and toLower()
public class ex136 {
    public static void main(String[] args) {
        String s1=new String("Hello");
        System.out.println("s1 = "+s1);
        System.out.println("InLower = "+s1.toLowerCase());
        System.out.println("Inupper = "+s1.toUpperCase());
    }
}
